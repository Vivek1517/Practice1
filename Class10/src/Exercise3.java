public class Exercise3 {
    public static void main(String[] args) {
        System.out.println("Generate greatest number: "+Math.max(40, 50));
        System.out.println("Generating round number to the closest '4.99': "+Math.floor(4.99));
        System.out.println("Generating round number to the next number '6.99': "+Math.ceil(6.99));
        System.out.println("Generating lowest number: "+Math.min(100,-1));
        System.out.println("Returning random value: "+Math.random());
        System.out.println("Giving pi value : "+Math.PI);
    }
}
